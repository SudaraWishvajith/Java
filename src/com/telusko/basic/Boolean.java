package com.telusko.basic;

public class Boolean {
    public static void main(String [] args){

        // boolean is your primitive data type
        // Boolean is your wrapper class
        boolean boolean_value1 =  true;
        boolean boolean_value2 =  false;
        System.out.println(boolean_value1);

        // to invert a variable
        boolean_value1 = !boolean_value1;
        System.out.println(boolean_value1);


    }
}
